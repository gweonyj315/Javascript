// Object 타입
// key_value
// Key는 중복 안됨 - 문자, 숫자
// value 중복 됨 = 아무타입가능
const americano = {
  name: "아메리카노",
  price: 2000,
  isIce: true,
  hasShot: true,
};
// dot. 연산자
window.console.log(americano.name); // 아메리카노
window.console.log(americano.price); // 2000
window.console.log(americano.hasShot); // true
// bracket[] 연산자
window.console.log(americano.name); // 아메리카노
window.console.log(americano.price); // 2000
window.console.log(americano.hasShot); // true
